	BITS 16
_START:
	XOR SI,SI
	MOV AX,0x1000
	MOV ES,AX
	XOR AX,AX
	MOV DS,AX
	MOV SS,AX
	MOV SP,0xFFFF
_LOAD:
	MOV AH,0x02
	MOV AL,0x40
	MOV BX,0x000
	MOV CX,2
	MOV DH,0
	INT 0x13
	JC  $ ; hang if error
_RUN:
	XOR SI,SI
	MOV AX,0x2000
	MOV ES,AX
	XOR AX,AX
	MOV DS,AX
	MOV SS,AX
	MOV SP,0xFFFF
	JMP 0x1000:0000

